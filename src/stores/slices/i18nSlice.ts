export const createI18nSlice: StateCreator<I18nSlice> = (set, get) => ({
  language: "en",
  setLanguage: (lang) => set({ language: lang }),
  t: (key) => {
    const lang = get().language;
    return translations[lang][key] ?? key;
  },
});
